// Helper functions
export const getCarById = (id) => carsData.find(car => car.id === parseInt(id));
export const getPropertyById = (id) => realEstateData.find(property => property.id === parseInt(id));

// Filter functions
export const filterCars = (filters) => {
    return carsData.filter(car => {
        if (filters.transmission && car.transmission !== filters.transmission) return false;
        if (filters.fuelType && car.fuelType !== filters.fuelType) return false;
        if (filters.minPrice && car.dailyRate < filters.minPrice) return false;
        if (filters.maxPrice && car.dailyRate > filters.maxPrice) return false;
        if (filters.seats && car.seats < filters.seats) return false;
        return true;
    });
};

export const filterProperties = (filters) => {
    return realEstateData.filter(property => {
        if (filters.propertyType && property.propertyType !== filters.propertyType) return false;
        if (filters.transactionType && property.transactionType !== filters.transactionType) return false;
        if (filters.minPrice && property.price < filters.minPrice) return false;
        if (filters.maxPrice && property.price > filters.maxPrice) return false;
        if (filters.bedrooms && property.bedrooms && property.bedrooms < filters.bedrooms) return false;
        if (filters.district && property.district !== filters.district) return false;
        return true;
    });
};
